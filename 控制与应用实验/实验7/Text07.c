void dh(unsigned char x,unsigned char y,unsigned char n,unsigned char fb) 
{
	unsigned char i,dd;
	for (i=0;i<16;i++) 
	{
		dd=hz[i+n*32];
		if (fb) //fb==1:反白显示
			if (n!=5) 
				dd=255-dd;
		lw(x*8+i,y,dd);//写数据

		dd=hz[i+n*32+16];
		if (fb)
	 		if (n!=5) 
				dd=255-dd;
		lw(x*8+i,y+1,dd);
	}
}

