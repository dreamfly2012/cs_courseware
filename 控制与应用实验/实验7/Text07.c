#include <reg52.h>
#include <intrins.h>
#include <stdio.h>
#define uchar unsigned char
#define uint unsigned int
#define ulong unsigned long

uint count_time=4000;
uint count,pcount;
sbit p_csa = P1^7; 	//cs1
sbit p_csb = P1^6;	//cs2
sbit p_rst = P1^5;   	
sbit p_rw = P3^4;	//读写信号		
sbit p_e = P3^3;	//使能信号
sbit p_rs = P3^5;	//寄存器选择信号


//判别busy与否
void lcd_busy(void) {
	P2=0xff;//总线释放
	p_rs=0;
	p_rw=1;
	while (1) {
		p_e=1;
		if (P2<0x80) break;//Not busy
		p_e=0;
	} 
	p_e=0;
}

//设置xy的位置
void set_xy(unsigned char x,unsigned char y) {
	if (x>=64)//右边屏幕有效64=0x0100 0000
 	{
	p_csa=0;
	p_csb=1;
	}
 	else   //左边有效
  	{
	p_csb=0;
	p_csa=1;
	}
	lcd_busy();
	p_rs=p_rw=0;//写指令代码
	P2=0x40|x; //0010 0000|x
	p_e=1;//p_e=1,写指令代码
	p_e=0;    
	lcd_busy();
	p_rs=p_rw=0;//写指令代码
	P2=0xb8|y;//1011 1000|y
	p_e=1;
	p_e=0;
	P2=0xff;  //总线释放
}


//写数据
void lw(unsigned char x,unsigned char y,unsigned char dd) {
	set_xy(x,y);//set x and y
	lcd_busy();
	p_rs=1;
	p_rw=0;//写displayed数据
	P2=dd;//data
	p_e=1;
	p_e=0;//下降边沿
	P2=0xff;//总线释放
}


//显示初始化
#pragma disable
void lcd_init(void) 
{
	unsigned char x,y;
	//p_gnda=p_gndb=0;
	/*开显示*/
	p_e=p_rs=p_rw=0;//现display开关设置
	p_csa=p_csb=0;//All closed at fist

	p_csa=1;//left open
	P2=0x3f;//0011 1111 open display开关
	p_e=1;
	p_e=0;//input BB7--DB0,写指令代码 
	p_csa=0;//left close

	p_csb=1;//right open
	P0=0x3f;//open display开关
	p_e=1;
	p_e=0;
	p_csb=0;//right close


	//0行开始显示
	p_csa=1;
	lcd_busy();
	p_rs=p_rw=0;//写指令代码
	P2=0xc0;   //1100 0000
	p_e=1;
	p_e=0;    //下降边沿
	p_csa=0;//close left

	p_csb=1;
	lcd_busy();
	p_rs=p_rw=0;
	P0=0xc0;
	p_e=1;
	p_e=0;
	p_csb=0;

	for (y=0;y<8;y++) {//列
		for (x=0;x<128;x++)//行
	 		lw(x,y,0);
	}
}

